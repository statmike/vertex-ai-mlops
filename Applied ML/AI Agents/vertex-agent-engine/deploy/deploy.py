"""Deploy an ADK agent to Vertex AI Agent Engine using source-file deployment.

This script demonstrates the full deployment lifecycle:
    - Package source code and dependencies
    - Pass environment variables from .env to the deployed agent
    - Create, update, delete, and test deployments
    - Track deployment state in a local JSON file

Usage:
    uv run python deploy/deploy.py                   # deploy
    uv run python deploy/deploy.py --update           # update existing
    uv run python deploy/deploy.py --delete           # delete
    uv run python deploy/deploy.py --info             # show deployment info
    uv run python deploy/deploy.py --test             # test deployed agent
    uv run python deploy/deploy.py --skip-local-test  # skip pre-deploy smoke test

What Agent Engine provides automatically (no config needed):
    - Persistent session management (cloud-based)
    - Cloud Monitoring (metrics dashboard)
    - Cloud Logging (runtime event logs)

What this script enables via env vars:
    - Cloud Trace: GOOGLE_CLOUD_AGENT_ENGINE_ENABLE_TELEMETRY=true
    - Prompt/response logging: OTEL_INSTRUMENTATION_GENAI_CAPTURE_MESSAGE_CONTENT=true
"""

