"""LLM judge — compares THINKING vs FAST answers with structured output.

Uses Gemini with structured output to evaluate answer quality on three
dimensions: groundedness, accuracy, and completeness. Returns a verdict
picking the better answer.
"""

from __future__ import annotations

import json
import logging
import os

from google import genai
from google.genai import types

from .schemas import JudgeVerdict

logger = logging.getLogger("eval.judge")

JUDGE_MODEL = os.environ.get("EVAL_JUDGE_MODEL", "gemini-2.5-flash")

_judge_client = None


def _get_client() -> genai.Client:
    """Return a cached genai Client for the judge."""
    global _judge_client
    if _judge_client is None:
        _judge_client = genai.Client(
            vertexai=True,
            project=os.environ.get("GOOGLE_CLOUD_PROJECT", ""),
            location=os.environ.get("TOOL_MODEL_LOCATION", "us-central1"),
        )
    return _judge_client


JUDGE_SYSTEM_PROMPT = """\
You are an expert evaluator comparing two answers to the same data question.
Both answers were generated by the same analytics system, but with different
thinking modes:
- Answer A used THINKING mode (slower, more deliberate SQL generation)
- Answer B used FAST mode (quicker, less deliberation)

Evaluate each answer on three dimensions:

1. **Groundedness** (0.0-1.0): Does the answer reference specific data values,
   table names, SQL results, or concrete numbers? An answer that says "the top
   value is 42" is more grounded than "there are some high values."

2. **Accuracy** (0.0-1.0): Based on the question and the data referenced, does
   the answer appear factually correct? Look for internal consistency, reasonable
   values, and proper interpretation of results.

3. **Completeness** (0.0-1.0): Does the answer address all parts of the
   question? If the question asks to "compare X across Y", does it actually
   compare rather than just list?

Pick a winner based on the overall quality. Choose "tie" only when both
answers are substantively equivalent — not when both are poor.

If one answer contains an error or failed to return data while the other
succeeded, the successful one wins regardless of other criteria.
"""


def judge_answers(
    question: str,
    thinking_answer: str,
    fast_answer: str,
    persona: str = "",
) -> JudgeVerdict:
    """Compare two answers and return a structured verdict.

    Args:
        question: The original user question.
        thinking_answer: Answer from THINKING mode.
        fast_answer: Answer from FAST mode.
        persona: The persona category (for context).

    Returns:
        JudgeVerdict with winner, reasoning, and per-answer scores.
    """
    client = _get_client()

    user_prompt = f"""\
## Question
{question}

## Persona
{persona}

## Answer A (THINKING mode)
{thinking_answer[:4000]}

## Answer B (FAST mode)
{fast_answer[:4000]}

Evaluate both answers and pick the better one.
"""

    response = client.models.generate_content(
        model=JUDGE_MODEL,
        contents=user_prompt,
        config=types.GenerateContentConfig(
            system_instruction=JUDGE_SYSTEM_PROMPT,
            response_mime_type="application/json",
            response_schema=JudgeVerdict.model_json_schema(),
            temperature=0.1,
        ),
    )

    verdict = JudgeVerdict.model_validate(json.loads(response.text))

    logger.info(
        "Judge verdict: winner=%s (thinking=%.2f/%.2f/%.2f, fast=%.2f/%.2f/%.2f) — %s",
        verdict.winner,
        verdict.thinking_score.groundedness,
        verdict.thinking_score.accuracy,
        verdict.thinking_score.completeness,
        verdict.fast_score.groundedness,
        verdict.fast_score.accuracy,
        verdict.fast_score.completeness,
        verdict.reasoning[:100],
    )

    return verdict
